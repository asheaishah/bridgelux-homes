Significant Dunman Road Site Up For Public Tender As Private Housing Supply Dips 
</h2>
<p class="max-w-5xl mx-auto text-justify">
In response to diminishing stocks of unsold new private housing, Singapore's government has put up a substantial site on Dunman Road for public tender as of Monday, March 14. This move follows closely on the heels of the tender launch for a smaller parcel of land in Pine Grove last week.
</p>
<h2 class="max-w-5xl mx-auto text-justify">
Dunman Road Site Details: A Potential Residential Hub
</h2>
<p class="max-w-5xl mx-auto text-justify">
Nestled next to Dakota MRT station and the Geylang River, the 99-year leasehold plot on Dunman Road spans 25,234.3 square meters and has a maximum gross floor area (GFA) of 88,321 square meters. According to the Urban Redevelopment Authority (URA), the site has the potential to accommodate up to 1,040 residential units.
</p>
<h2 class="max-w-5xl mx-auto text-justify">
Assessing Developer Appetite for Large-Scale Projects
</h2>
<p class="max-w-5xl mx-auto text-justify">
The Dunman Road site is seen as a potential litmus test for developers' interest in undertaking large-scale projects, according to Huttons Asia's senior director of research, Lee Sze Teck. The most recent similar venture was the launch of Normanton Park. Positive participation in the Dunman Road Government Land Sales (GLS) tender could boost confidence in forthcoming large-scale en-bloc developments, Teck explained.
</p>
<h2 class="max-w-5xl mx-auto text-justify">
Tender Details and Potential Developer Strategies
</h2>
<p class="max-w-5xl mx-auto text-justify">
The tender, which is part of the confirmed list of the GLS programme for the first half of the year, will conclude on June 2. The Pine Grove parcel A site's tender will also close on the same date. As for the sizeable Dunman Road site, it's expected that the bidding process may inspire more joint ventures due to the expected investment quantum exceeding $1 billion, according to Steven Tan, CEO of OrangeTee & Tie.
</p>
<h2 class="max-w-5xl mx-auto text-justify">
Future Developments: Positive Projections and Potential Challenges
</h2>
<p class="max-w-5xl mx-auto text-justify">
Despite the size of the site, PropNex Realty's head of research and content, Ms. Wong Siew Ying, believes developers may not be daunted. Given the impressive take-up rates of over 90% for several large projects in recent years and a relatively limited supply of new private homes in the vicinity, future residences on this site should attract buyers. The site's proximity to the Dakota MRT station, as well as its many nearby amenities and schools, add to its appeal.
</p>
<h2 class="max-w-5xl mx-auto text-justify">
Risk Factors and Developer Confidence
</h2>
<p class="max-w-5xl mx-auto text-justify">
However, the Dunman Road site does carry heightened development risks. Developers may face a 35% additional buyers stamp duty if they fail to sell all units within five years. Despite these potential risks, recent land sales have witnessed robust tenders, indicating developers' confidence in the local residential market. As an example, two freehold development land parcels were recently sold en bloc for $815 million or $1,488 psf ppr in Thiam Siew Avenue.
